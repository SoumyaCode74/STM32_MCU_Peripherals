void generate_unit_impulse(float32_t * sig, unsigned int N, unsigned int interval){
	unsigned int i = 0;
	while (i < N){
		if (i % interval == 0)
			sig[i] = 1;
		else
			sig[i] = 0;
		i++;
	}
}

void generate_reference_step(float32_t * y_ref, uint32_t N, uint32_t mag){
	unsigned int i = 0;
		while (i < N){
			y_ref[i] = mag*1;
			i++;
		}
}

void generate_reference_ramp(float32_t * y_ref, uint32_t N, uint32_t interval){
	unsigned int i = 0;
	while (i < N){
		for (int j = 0; j < interval; j++){
		y_ref[i+j] = j;
		}
		i += interval;
	}
}
void generate_reference_parabolic(float32_t * y_ref, uint32_t N, uint32_t interval){
	unsigned int i = 0;
	while (i < N){
		for (int j = 0; j < interval; j++){
		y_ref[i+j] = pow(j, 2);
		}
		i += interval;
	}
}
void generate_reference_sine(float32_t * y_ref, uint32_t N, uint32_t interval, float32_t freq, uint32_t phi){
	unsigned int i = 0;
	while (i < N){
		for (int j = 0; j < interval; j++){
		y_ref[i+j] = sin((freq*j)+phi*(PI/180));
		}
		i += interval;
	}
}

void generate_reference_pulse(float32_t * y_ref, uint32_t N, uint32_t on_time, float32_t peak){
	unsigned int i = 0;
	while (i < N){
		if (i < on_time)
			y_ref[i] = peak;
		else
			y_ref[i] = 0;
		i++;
	}
}

void generate_reference_square(float32_t * y_ref, uint32_t N, uint32_t on_time, float32_t peak_to_peak){
	unsigned int i = 0;
	while (i < N){
		if (i < on_time)
			y_ref[i] = (peak_to_peak/2.0);
		else
			y_ref[i] = -(peak_to_peak/2.0);
		i++;
	}
}
